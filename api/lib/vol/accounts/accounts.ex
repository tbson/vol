defmodule Vol.Accounts do
  @moduledoc """
  The Accounts context.
  """

  import Ecto.Query, warn: false
  alias Vol.Repo

  alias Vol.Accounts.User
  alias Vol.Guardian
  import Comeonin.Argon2, only: [checkpw: 2, dummy_checkpw: 0]

  @doc """
  Returns the list of users.

  ## Examples

      iex> list_users()
      [%User{}, ...]

  """
  def list_users do
    Repo.all(User)
  end

  @doc """
  Gets a single user.

  Raises `Ecto.NoResultsError` if the User does not exist.

  ## Examples

      iex> get_user!(123)
      %User{}

      iex> get_user!(456)
      ** (Ecto.NoResultsError)

  """
  def get_user!(id), do: Repo.get!(User, id)

  defp get_user_by_email(%{email: email} = auth_input) do
    case from(u in User, join: c in assoc(u, :credential), where: c.email == ^email)
         |> Repo.one()
         |> Repo.preload(:credential) do
      user ->
        %{auth_input | user: user}

      nil ->
        dummy_checkpw()
        %{auth_input | err: :unauthorized}
    end
  end

  defp verify_user_password(%{password: password, user: user} = auth_input) do
    case checkpw(password, user.credential.password_hash) do
      true ->
        auth_input

      _ ->
        %{auth_input | user: nil, err: :unauthorized}
    end
  end

  def get_token(%{user: user} = auth_input) do
    case user do
      nil ->
        %{auth_input | err: :unauthorized}

      _ ->
        %{auth_input | token: Guardian.encode_and_sign(user)}
    end
  end

  def token_auth(email, password) do
    %{email: email, password: password, user: nil, token: nil, err: nil}
    |> get_user_by_email()
    |> verify_user_password()
    |> get_token()
  end

  @doc """
  Creates a user.

  ## Examples

      iex> create_user(%{field: value})
      {:ok, %User{}}

      iex> create_user(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_user(attrs \\ %{}) do
    %User{}
    |> User.registration_changeset(attrs)
    |> Repo.insert()
  end

  @doc """
  Updates a user.

  ## Examples

      iex> update_user(user, %{field: new_value})
      {:ok, %User{}}

      iex> update_user(user, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_user(%User{} = user, attrs) do
    user
    |> User.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a User.

  ## Examples

      iex> delete_user(user)
      {:ok, %User{}}

      iex> delete_user(user)
      {:error, %Ecto.Changeset{}}

  """
  def delete_user(%User{} = user) do
    Repo.delete(user)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking user changes.

  ## Examples

      iex> change_user(user)
      %Ecto.Changeset{source: %User{}}

  """
  def change_user(%User{} = user) do
    User.changeset(user, %{})
  end

